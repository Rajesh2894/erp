package com.abm.mainet.property.dto;

public class WitnessDTO {

    
    private String witnessName;
    private String witnessContactNo;
    private String witnessAddress;
    private String witnessPANNo;
    public String getWitnessName() {
        return witnessName;
    }
    public void setWitnessName(String witnessName) {
        this.witnessName = witnessName;
    }
    public String getWitnessContactNo() {
        return witnessContactNo;
    }
    public void setWitnessContactNo(String witnessContactNo) {
        this.witnessContactNo = witnessContactNo;
    }
    public String getWitnessAddress() {
        return witnessAddress;
    }
    public void setWitnessAddress(String witnessAddress) {
        this.witnessAddress = witnessAddress;
    }
    public String getWitnessPANNo() {
        return witnessPANNo;
    }
    public void setWitnessPANNo(String witnessPANNo) {
        this.witnessPANNo = witnessPANNo;
    }
    
    
    
}
