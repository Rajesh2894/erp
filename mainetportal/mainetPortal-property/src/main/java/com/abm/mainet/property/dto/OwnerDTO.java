package com.abm.mainet.property.dto;

public class OwnerDTO {

    private String ownerName;
    private String ownerContactNo;
    private String ownerAddress;
    private String ownerPANNo;

    public String getOwnerName() {
        return ownerName;
    }

    public void setOwnerName(String ownerName) {
        this.ownerName = ownerName;
    }

    public String getOwnerContactNo() {
        return ownerContactNo;
    }

    public void setOwnerContactNo(String ownerContactNo) {
        this.ownerContactNo = ownerContactNo;
    }

    public String getOwnerAddress() {
        return ownerAddress;
    }

    public void setOwnerAddress(String ownerAddress) {
        this.ownerAddress = ownerAddress;
    }

    public String getOwnerPANNo() {
        return ownerPANNo;
    }

    public void setOwnerPANNo(String ownerPANNo) {
        this.ownerPANNo = ownerPANNo;
    }

}
