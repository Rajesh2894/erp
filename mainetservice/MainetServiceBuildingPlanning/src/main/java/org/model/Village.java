package org.model;

public class Village {

	
	private String name;
	private String code;
	private String censusCode;
	private String khewats;
	private String khatonis;
	private String khasras;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getCensusCode() {
		return censusCode;
	}
	public void setCensusCode(String censusCode) {
		this.censusCode = censusCode;
	}
	public String getKhewats() {
		return khewats;
	}
	public void setKhewats(String khewats) {
		this.khewats = khewats;
	}
	public String getKhatonis() {
		return khatonis;
	}
	public void setKhatonis(String khatonis) {
		this.khatonis = khatonis;
	}
	public String getKhasras() {
		return khasras;
	}
	public void setKhasras(String khasras) {
		this.khasras = khasras;
	}
	
	
	
	
}
